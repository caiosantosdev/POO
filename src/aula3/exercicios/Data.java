package aula3.exercicios;

public class Data {
	private int dia;
	private int mes;
	private int ano;
	public int getDia() {
		return dia;
	}
	public void setDia(int d) {
		dia = d;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int m) {
		mes = m;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int a) {
		ano = a;
	}
}
